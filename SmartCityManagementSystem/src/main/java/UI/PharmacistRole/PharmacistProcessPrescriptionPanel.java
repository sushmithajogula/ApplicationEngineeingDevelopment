    private void jButtongoto2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtongoto2ActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = jTablepresctable.getSelectedRow();
        
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a prescription.");
            return;
        }
        
        DefaultTableModel records = (DefaultTableModel) jTablepresctable.getModel();
        Integer id = (Integer) records.getValueAt(selectedRowIndex, 0);
        
        Prescription prescription = pharmacist.getPresdirectory().getPrescription(id);
        
        Patient patient;
        Prescription prescription1 = new Prescription();
        
        for(Patient p : pharmacy.getPatientDirectory().getPatientList()){
            for(Prescription i : p.getPresdirectory().getPrescriptionList()){
                if(i.equals(prescription)){
                   patient = p;
                   prescription1 = i;
                }
            }
        }
        
        prescription.setStatus("Completed");
        prescription1.setStatus("Completed");
        
        populateTable();
        JOptionPane.showMessageDialog(this, "Prescription Processed.");
    }//GEN-LAST:event_jButtongoto2ActionPerformed


