private void populateTable() {
//        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        
        DefaultTableModel records = (DefaultTableModel) jTablepresctable.getModel();
        records.setRowCount(0); //Clear empty records
        for(Prescription o : pharmacist.getPresdirectory().getPrescriptionList()){
            Object[] row = new Object[2];
            row[0] = o.getId();
            row[1] = o.getStatus();
            records.addRow(row);
        }
    }

    private void populateTable2(Prescription prescription) {
//        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        
        DefaultTableModel records = (DefaultTableModel) jTablepharmname.getModel();
        records.setRowCount(0); //Clear empty records
        for(String o : prescription.getDrugs()){
            Object[] row = new Object[1];
            row[0] = o;
            records.addRow(row);
        }        
    }
