    private void jButtoncreateprescreqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtoncreateprescreqActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = jTablepresctable.getSelectedRow();
        
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a prescription.");
            return;
        }
        
        DefaultTableModel records = (DefaultTableModel) jTablepresctable.getModel();
        Integer id = (Integer) records.getValueAt(selectedRowIndex, 0);
        
        Prescription precription = patient.getPresdirectory().getPrescription(id);
        
        selectedRowIndex = jTablepharmname.getSelectedRow();
        
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a pharmacy.");
            return;
        }
        
        records = (DefaultTableModel) jTablepharmname.getModel();
        String name = (String) records.getValueAt(selectedRowIndex, 0);
        
        Pharmacy pharmacy = business.getPharmacyDirectory().getPharmacy(name);
        
        selectedRowIndex = jTablepharmacist.getSelectedRow();
        
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a pharmacy.");
            return;
        }
        
        records = (DefaultTableModel) jTablepharmacist.getModel();
        id = (Integer) records.getValueAt(selectedRowIndex, 0);
        
        Pharmacist pharmacist = pharmacy.getPharmacistDirectory().getPharmacist(id);
        
        precription.setStatus("Active");
        
        pharmacist.getPresdirectory().getPrescriptionList().add(precription);
        pharmacy.getPrescriptionDirectory().getPrescriptionList().add(precription);
        
        pharmacy.getPatientDirectory().getPatientList().add(patient);
        JOptionPane.showMessageDialog(this, "Prescription Request Created");
    }//GEN-LAST:event_jButtoncreateprescreqActionPerformed


