    private void jTableorderlistMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableorderlistMousePressed
        // TODO add your handling code here:
        int selectedRowIndex = jTableorderlist.getSelectedRow();

        if(selectedRowIndex < 0){
            ratingtxt.setText("");
            JOptionPane.showMessageDialog(this, "Please select a row.");
            return;
        }

        DefaultTableModel records = (DefaultTableModel) jTableorderlist.getModel();
        switch(type) {
            case "Shop":
                String name = (String) records.getValueAt(selectedRowIndex, 0);
                int id = Integer.parseInt(name.substring(10));
                ratingtxt.setText(customer.getShopOrderDirectory().getShopOrder(id).getComments());
                break;
            case "Restaurant":
                String name1 = (String) records.getValueAt(selectedRowIndex, 0);
                int id1 = Integer.parseInt(name1.substring(6));
                ratingtxt.setText(customer.getOrderDirectory().getOrder(id1).getComments());
                break;
            case "Theatre":
                ratingtxt.setText("");
                break;
            default:
                JOptionPane.showMessageDialog(this, "Please select Shop, Movie, or Restaurant.");
                break;
        }
    }//GEN-LAST:event_jTableorderlistMousePressed


