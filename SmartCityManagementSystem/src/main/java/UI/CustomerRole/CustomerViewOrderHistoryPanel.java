    private void populateTable() {
//        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        DefaultTableModel records = (DefaultTableModel) jTableorderlist.getModel();
                
        switch(type) {
            case "Shop":
                records.setRowCount(0); //Clear empty records
                for(ShopOrder o : customer.getShopOrderDirectory().getOrderList()){
                    Object[] row = new Object[3];
                    row[0] = "ShopOrder-"+o.getId();
                    row[1] = o.getCost();
                    row[2] = o.getStatus();
                    records.addRow(row);
                }
                break;
            case "Restaurant":
                records.setRowCount(0); //Clear empty records
                for(Order o : customer.getOrderDirectory().getOrderList()){
                    Object[] row = new Object[3];
                    row[0] = "Order-"+o.getId();
                    row[1] = o.getCost();
                    row[2] = o.getStatus();
                    records.addRow(row);
                }
                break;
            case "Theatre":
                records.setRowCount(0); //Clear empty records
                for(String o : customer.getTheatreDirectory().keySet()){
                    Object[] row = new Object[3];
                    row[0] = "Movie-"+o;
                    row[1] = customer.getTheatreDirectory().get(o);
                    row[2] = "Watched";
                    records.addRow(row);
                }
                break;
            default:
                JOptionPane.showMessageDialog(this, "Please select Shop, Movie, or Restaurant.");
                break;
        }
    }
